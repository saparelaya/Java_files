package practice;

public class RelationalOperators {

	public static void main(String[] args) {
		int a=65,b=13;
		
		System.out.println("a==b:" +(a==b));
		System.out.println("a!=b:" +(a!=b));
		System.out.println("a>b:" +(a>b));
		System.out.println("a<b:" +(a<b));
		System.out.println("a>=b:" +(a>=b));
		System.out.println("a<=b:" +(a<=b));

	}

}
